CProtoCliGT::CProtoCliGT()
{
    m_vecMsgList.clear() ;
	memset(m_EncodeFuncArray, 0, sizeof(m_EncodeFuncArray));
	memset(m_DecodeFuncArray, 0, sizeof(m_DecodeFuncArray));
	m_iMaxPkgSize = 0;
	Init();
}

CProtoCliGT * CProtoCliGT::Instance()
{
   static CProtoCliGT oInst;
   return &oInst;
}

CProtoCliGT::~CProtoCliGT()
{
}

INT32 CProtoCliGT::Encode(INT32 iMsgID, void *pHost, CHAR *pNet, INT32 iNetSize)
{
	m_oData.Prepare(pNet, iNetSize, 1, 3, 2);

	EnFuncCProtoCliGT pfnEncode = FindEncodeFunc(iMsgID);
	if(NULL == pfnEncode)
		return -1;

	return (*pfnEncode)(pHost, &m_oData);
}

INT32 CProtoCliGT::Decode(INT32 iMsgID, CHAR *pNet,INT32 iNetSize, void *pHost, INT32 iHostSize)
{
	m_oData.Prepare(pNet, iNetSize, 1, 3, 2);
	if(iHostSize < m_iMaxPkgSize)
		return -1;

	DeFuncCProtoCliGT pfnDecode = FindDecodeFunc(iMsgID);
	if(NULL == pfnDecode)
		return -1;

	return (*pfnDecode)(pHost, &m_oData);
}

EnFuncCProtoCliGT CProtoCliGT::FindEncodeFunc(INT32 iMsgID)
{
	if(iMsgID - PROTOCLIGT_MSGID_OFFSET >= PROTOCLIGT_MSGID_RANGE)
		return NULL;
	return m_EncodeFuncArray[iMsgID - PROTOCLIGT_MSGID_OFFSET];
}

DeFuncCProtoCliGT CProtoCliGT::FindDecodeFunc(INT32 iMsgID)
{
	if(iMsgID - PROTOCLIGT_MSGID_OFFSET >= PROTOCLIGT_MSGID_RANGE)
		return NULL;
	return m_DecodeFuncArray[iMsgID - PROTOCLIGT_MSGID_OFFSET];
}

